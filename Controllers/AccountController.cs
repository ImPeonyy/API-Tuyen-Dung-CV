            SqlDataReader myReader;
            using (SqlConnection myCon = new SqlConnection(sqlDataSource))
            {
                myCon.Open();
                using (SqlCommand myCommand = new SqlCommand(query, myCon))
                {
                    myCommand.Parameters.AddWithValue("@password", acc.password);
                    myCommand.Parameters.AddWithValue("@name", acc.name);
                    myCommand.Parameters.AddWithValue("@email", acc.email);
                    myCommand.Parameters.AddWithValue("@phone_number", acc.phone_number);
                    myCommand.Parameters.AddWithValue("@role", acc.role);
                    myReader = myCommand.ExecuteReader();
                    table.Load(myReader);
                    myReader.Close();
                    myCon.Close();
                }
            }
            return new JsonResult("Create Successfully!");
        }

